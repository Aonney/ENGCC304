#include <stdio.h>

/**
 * โปรแกรมตรวจสอบเกรดตามคะแนนดิบ โดยใช้คำสั่ง if-else เท่านั้น
 *
 * เกณฑ์การให้คะแนน:
 * F  : < 50
 * D  : 50 <= score < 55
 * D+ : 55 <= score < 60
 * C  : 60 <= score < 65
 * C+ : 65 <= score < 70
 * B  : 70 <= score < 75
 * B+ : 75 <= score < 80
 * A  : >= 80
 */
int main() {
    // 1. ประกาศตัวแปรสำหรับเก็บคะแนนดิบ
    int score;

    // 2. รับค่าคะแนนดิบจากผู้ใช้
    printf("enter score :\n");
    
    // ตรวจสอบความถูกต้องของการป้อนค่า
    if (scanf("%d", &score) != 1) {
        printf("Invalid input. Please enter a number.\n");
        return 1;
    }

    // 3. ตรวจสอบเกรดโดยใช้คำสั่ง if-else เท่านั้น
    // เนื่องจากเงื่อนไขต้องครอบคลุมคะแนนตั้งแต่ 0 ถึง 100 
    // และ A เริ่มที่ > 80, B+ ที่ 75-80 เราจะเริ่มตรวจสอบจากเกรดสูงสุดลงมา

    if (score >= 80) {
        // A อยู่ในช่วงคะแนนมากกว่า 80 ขึ้นไป
        printf("A !\n");
    } 
    else if (score >= 75) {
        // B+ อยู่ในช่วงระหว่าง 75 ถึง 80 (หมายถึง 75 <= score < 80)
        printf("B+ !\n");
    } 
    else if (score >= 70) {
        // B อยู่ในช่วงระหว่าง 70 ถึง 75 (หมายถึง 70 <= score < 75)
        printf("B !\n");
    } 
    else if (score >= 65) {
        // C+ อยู่ในช่วงระหว่าง 65 ถึง 70 (หมายถึง 65 <= score < 70)
        printf("C+ !\n");
    } 
    else if (score >= 60) {
        // C อยู่ในช่วงระหว่าง 60 ถึง 65 (หมายถึง 60 <= score < 65)
        printf("C !\n");
    } 
    else if (score >= 55) {
        // D+ อยู่ในช่วงระหว่าง 55 ถึง 60 (หมายถึง 55 <= score < 60)
        printf("D+ !\n");
    } 
    else if (score >= 50) {
        // D อยู่ในช่วงระหว่าง 50 ถึง 55 (หมายถึง 50 <= score < 55)
        printf("D !\n");
    } 
    else { 
        // F อยู่ในช่วงคะแนนน้อยกว่า 50
        printf("F !\n");
    }

    return 0;
}
