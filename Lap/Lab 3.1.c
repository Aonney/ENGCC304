#include <stdio.h>

/**
 * โปรแกรมคำนวณหาพื้นที่สามเหลี่ยมจากส่วนสูงและความยาวฐาน
 * สูตร: Area = (Height * Base) / 2
 */
int main() {
    // 1. ประกาศตัวแปร
    // ใช้ float เพื่อรองรับค่าทศนิยมของส่วนสูง ฐาน และพื้นที่
    float height, base, area;

    // 2. รับค่าจากผู้ใช้ (ส่วนสูง)
    printf("--- โปรแกรมคำนวณพื้นที่สามเหลี่ยม ---\n");
    printf("กรุณาป้อนค่าส่วนสูง (H): ");
    
    // ใช้ &height เพื่อชี้ไปยังตำแหน่งหน่วยความจำของตัวแปร height
    if (scanf("%f", &height) != 1) {
        printf("⚠️ ข้อผิดพลาด: การป้อนค่าส่วนสูงไม่ถูกต้อง\n");
        return 1;
    }

    // 3. รับค่าจากผู้ใช้ (ฐาน)
    printf("กรุณาป้อนค่าฐาน (W): ");
    
    // ใช้ &base เพื่อชี้ไปยังตำแหน่งหน่วยความจำของตัวแปร base
    if (scanf("%f", &base) != 1) {
        printf("⚠️ ข้อผิดพลาด: การป้อนค่าฐานไม่ถูกต้อง\n");
        return 1;
    }

    // 4. คำนวณหาพื้นที่สามเหลี่ยม
    area = (height * base) / 2.0;

    // 5. แสดงผลลัพธ์
    printf("\n--- ผลลัพธ์ ---\n");
    printf("ส่วนสูงที่ป้อน (H) คือ: %.2f\n", height);
    printf("ฐานที่ป้อน (W) คือ: %.2f\n", base);
    // ใช้ %.1f เพื่อแสดงทศนิยม 1 ตำแหน่งตาม Test Case
    printf("พื้นที่สามเหลี่ยม (Area) คือ: %.1f\n", area);

    return 0;
}
