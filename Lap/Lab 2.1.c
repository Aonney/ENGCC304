#include <stdio.h>
#include <stdbool.h> // สำหรับใช้ชนิดข้อมูล bool (true/false)

// กำหนดค่าคงที่ (ถ้ามี)
#define NUM_STEPS 15

int main() {
    // ตัวแปรสำหรับจำลองการตัดสินใจ
    bool is_crispy_pork_ready = false; // สมมติว่ายังไม่พร้อม, ต้องทอดเอง
    bool is_taste_ok = false;           // สมมติว่ารสชาติยังไม่ถูกปาก

    printf("--- เริ่มต้นทำข้าวผัดกระเพรากรอบหมูกรอบ+ไข่เยี่ยวม้า ---\n\n");

    // 1. การเตรียมการ (Preparation)
    printf("ขั้นตอนที่ 1: การเตรียมการ\n");
    printf("1.1. เตรียมวัตถุดิบ (หมูกรอบ/ใบกระเพรา/พริก/กระเทียม/ไข่เยี่ยวม้า)\n");
    printf("1.2. ตวงเครื่องปรุงและเตรียมอุปกรณ์ครัว (จาน ช้อน ตะหลิว กระทะ)\n");
    printf("1.3. ตักข้าวสวยใส่จานเตรียมไว้\n\n");

    // 2. การปรุงหมูกรอบและไข่เยี่ยวม้า (Pre-cooking)
    printf("ขั้นตอนที่ 2: การปรุงหมูกรอบและไข่เยี่ยวม้า\n");
    
    // Condition 1: หมูกรอบพร้อมไหม?
    if (is_crispy_pork_ready) {
        printf("2.1. (ใช่) หมูกรอบพร้อมแล้ว! ไปขั้นตอนต่อไป\n");
    } else {
        printf("2.1. (ไม่) หมูกรอบยังไม่พร้อม:\n");
        printf("     > ทอดหมูสามชั้นให้กรอบ แล้วหั่นเป็นชิ้นพอดีคำ\n");
    }

    // Process 4: ทอดไข่เยี่ยวม้า
    printf("2.2. นำไข่เยี่ยวม้าลงทอดในน้ำมันร้อน ตักพักไว้เมื่อกรอบนอกนุ่มใน\n\n");

    // 3. การปรุงผัดกระเพรา (Stir-frying)
    printf("ขั้นตอนที่ 3: การปรุงผัดกระเพรา\n");
    
    // Process 5: ผัดพริกกระเทียม
    printf("3.1. ตำ/สับพริกกับกระเทียม แล้วนำไปผัดกับน้ำมันจนหอม\n");

    // Process 6: ปรุงรสและผัดหลัก
    printf("3.2. ใส่หมูกรอบและไข่เยี่ยวม้าที่ทอดแล้วลงไป ปรุงรสด้วยน้ำปลา น้ำตาล ซีอิ๊ว\n");
    
    // Condition 2: รสชาติ
    do {
        if (is_taste_ok) {
            printf("3.3. (ใช่) รสชาติถูกปากแล้ว! ไปขั้นตอนสุดท้าย\n");
        } else {
            printf("3.3. (ไม่) รสชาติยังไม่ถูกปาก:\n");
            printf("     > เติมเครื่องปรุง/น้ำเปล่าเล็กน้อย ผัดต่อจนได้รสที่ต้องการ\n");
            // ในโค้ดจริงต้องรับ Input เพื่อเปลี่ยนค่า is_taste_ok
            // แต่ในที่นี้จะจำลองให้เป็น True ในรอบถัดไปเพื่อจบ Loop
            is_taste_ok = true; 
        }
    } while (!is_taste_ok);

    // Process 7: ใส่ใบกระเพรา
    printf("3.4. ใส่ใบกระเพรากรอบ ผัดเร็ว ๆ แล้วปิดไฟ\n\n");

    // Output & Stop
    printf("--- ✅สิ้นสุดกระบวนการปรุง ---\n");
    printf("4. ตักผัดกระเพราใส่จานข้าวสวย พร้อมเสิร์ฟ\n");

    return 0;
}
