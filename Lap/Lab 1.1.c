#include <stdio.h>

int main() {
    // ประกาศตัวแปรสำหรับเก็บหมายเลข 3 ค่าและผลรวม
    int num1, num2, num3;
    int sum;

    // แสดงข้อความแนะนำให้ผู้ใช้ป้อนหมายเลข
    printf("กรอกหมายเลขตัวแรก: ");
    // รับค่าหมายเลขตัวแรก
    scanf("%d", &num1);

    printf("กรอกหมายเลขตัวที่สอง: ");
    // รับค่าหมายเลขตัวที่สอง
    scanf("%d", &num2);

    printf("กรอกหมายเลขตัวที่สาม: ");
    // รับค่าหมายเลขตัวที่สาม
    scanf("%d", &num3);

    // คำนวณผลรวม
    sum = num1 + num2 + num3;

    // แสดงผลลัพธ์ตามรูปแบบที่กำหนด
    printf("Answer = %d\n", sum);

    return 0;
}
