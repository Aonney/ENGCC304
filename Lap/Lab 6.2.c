#include <stdio.h>

int main() {
    int N;
    int i; // ตัวแปรสำหรับใช้ใน Loop

    // 1. รับค่าตัวเลขจากผู้ใช้
    printf("Enter value:\n");
    if (scanf("%d", &N) != 1) {
        printf("Invalid input.\n");
        return 1;
    }

    printf("Series: ");

    // 2. ตรวจสอบเงื่อนไข: เลขคี่ (N % 2 != 0) หรือ เลขคู่ (N % 2 == 0)
    
    if (N % 2 != 0) {
        // เงื่อนไข: ผู้ใช้กรอกเลขคี่
        
        // แสดงลำดับตัวเลขคี่ ตั้งแต่ 1 ถึง N (เพิ่มทีละ 2)
        for (i = 1; i <= N; i += 2) {
            printf("%d ", i);
        }
    } 
    else {
        // เงื่อนไข: ผู้ใช้กรอกเลขคู่
        
        // แสดงลำดับตัวเลขคู่ ตั้งแต่ N ถึง 0 (ลดลงทีละ 2)
        for (i = N; i >= 0; i -= 2) {
            printf("%d ", i);
        }
    }

    printf("\n"); // ขึ้นบรรทัดใหม่เมื่อจบ Series
    return 0;
}
